# For loop

for i in range(10): # range (10) mean i from 0 to 9
    print(i)  # prints numbers from 0 to 9

print(end="")

for i in range(2,4):
    print(i)  # prints numbers from 2 to 3
    
sum = 0;
for i in range(10):
    sum+=i
print(sum);

# While loop

i = 10;
while i > 0:
    print(i)
    i = i-1;